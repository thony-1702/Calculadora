package app.sethtec.calculadora

class Calculadora : Metodos {
    override fun suma(num1: Double, num2: Double) = num1 + num2
    override fun resta(num1: Double, num2: Double) = num1 - num2
    override fun multiplicacion(num1: Double, num2: Double) = num1 * num2
    override fun division(num1: Double, num2: Double) = num1 / num2 // corregido
}
